package tachiyomi.data.entry

import eu.kanade.tachiyomi.source.entry.EntryType
import eu.kanade.tachiyomi.source.entry.EntryUpdateStrategy
import kotlinx.serialization.json.JsonObject
import tachiyomi.data.Chapters
import tachiyomi.data.Entries
import tachiyomi.domain.entry.model.Entry
import tachiyomi.domain.entry.model.EntryChapter
import tachiyomi.domain.entry.model.EntryStatus

object EntryMapper {

    fun mapEntry(entry: Entries): Entry {
        return mapEntry(
            id = entry._id,
            profileId = entry.profile_id,
            source = entry.source,
            url = entry.url,
            title = entry.title,
            displayName = entry.display_name,
            artist = entry.artist,
            author = entry.author,
            description = entry.description,
            genre = entry.genre,
            status = entry.status,
            thumbnailUrl = entry.thumbnail_url,
            favorite = entry.favorite,
            lastUpdate = entry.last_update,
            nextUpdate = entry.next_update,
            initialized = entry.initialized,
            viewerFlags = entry.viewer,
            chapterFlags = entry.chapter_flags,
            coverLastModified = entry.cover_last_modified,
            dateAdded = entry.date_added,
            updateStrategy = entry.update_strategy,
            calculateInterval = entry.calculate_interval,
            lastModifiedAt = entry.last_modified_at,
            favoriteModifiedAt = entry.favorite_modified_at,
            version = entry.version,
            isSyncing = entry.is_syncing,
            notes = entry.notes,
            memo = entry.memo,
            type = entry.type,
        )
    }

    @Suppress("UNUSED_PARAMETER")
    fun mapEntry(
        id: Long,
        profileId: Long,
        source: Long,
        url: String,
        title: String,
        displayName: String?,
        artist: String?,
        author: String?,
        description: String?,
        genre: List<String>?,
        status: Long,
        thumbnailUrl: String?,
        favorite: Boolean,
        lastUpdate: Long?,
        nextUpdate: Long?,
        initialized: Boolean,
        viewerFlags: Long,
        chapterFlags: Long,
        coverLastModified: Long,
        dateAdded: Long,
        updateStrategy: EntryUpdateStrategy,
        calculateInterval: Long,
        lastModifiedAt: Long,
        favoriteModifiedAt: Long?,
        version: Long,
        isSyncing: Boolean,
        notes: String,
        memo: JsonObject,
        type: String,
    ): Entry = Entry(
        id = id,
        source = source,
        url = url,
        title = title,
        displayName = displayName,
        artist = artist,
        author = author,
        description = description,
        genre = genre,
        status = EntryStatus.from(status.toInt()),
        thumbnailUrl = thumbnailUrl,
        favorite = favorite,
        lastUpdate = lastUpdate ?: 0L,
        nextUpdate = nextUpdate ?: 0L,
        initialized = initialized,
        viewerFlags = viewerFlags,
        chapterFlags = chapterFlags,
        coverLastModified = coverLastModified,
        dateAdded = dateAdded,
        updateStrategy = updateStrategy,
        fetchInterval = calculateInterval.toInt(),
        lastModifiedAt = lastModifiedAt,
        favoriteModifiedAt = favoriteModifiedAt,
        version = version,
        isSyncing = isSyncing,
        notes = notes,
        memo = memo,
        type = EntryType.valueOf(type.uppercase()),
        profileId = profileId,
    )

    @Suppress("UNUSED_PARAMETER", "LongParameterList")
    fun mapLibraryEntry(
        id: Long,
        profileId: Long,
        source: Long,
        url: String,
        title: String,
        displayName: String?,
        artist: String?,
        author: String?,
        description: String?,
        genre: List<String>?,
        status: Long,
        thumbnailUrl: String?,
        favorite: Boolean,
        lastUpdate: Long?,
        nextUpdate: Long?,
        initialized: Boolean,
        viewerFlags: Long,
        chapterFlags: Long,
        coverLastModified: Long,
        dateAdded: Long,
        updateStrategy: EntryUpdateStrategy,
        calculateInterval: Long,
        lastModifiedAt: Long,
        favoriteModifiedAt: Long?,
        version: Long,
        isSyncing: Boolean,
        notes: String,
        memo: JsonObject,
        type: String,
        totalCount: Long,
        readCount: Double,
        latestUpload: Long,
        chapterFetchedAt: Long,
        lastRead: Long,
        bookmarkCount: Double,
        categories: String,
    ): Entry = mapEntry(
        id = id,
        profileId = profileId,
        source = source,
        url = url,
        title = title,
        displayName = displayName,
        artist = artist,
        author = author,
        description = description,
        genre = genre,
        status = status,
        thumbnailUrl = thumbnailUrl,
        favorite = favorite,
        lastUpdate = lastUpdate,
        nextUpdate = nextUpdate,
        initialized = initialized,
        viewerFlags = viewerFlags,
        chapterFlags = chapterFlags,
        coverLastModified = coverLastModified,
        dateAdded = dateAdded,
        updateStrategy = updateStrategy,
        calculateInterval = calculateInterval,
        lastModifiedAt = lastModifiedAt,
        favoriteModifiedAt = favoriteModifiedAt,
        version = version,
        isSyncing = isSyncing,
        notes = notes,
        memo = memo,
        type = type,
    )

    fun mapChapter(chapter: Chapters): EntryChapter {
        return mapChapter(
            id = chapter._id,
            entryId = chapter.entry_id,
            url = chapter.url,
            name = chapter.name,
            read = chapter.read,
            bookmark = chapter.bookmark,
            chapterNumber = chapter.chapter_number,
            scanlator = chapter.scanlator,
            dateUpload = chapter.date_upload,
            dateFetch = chapter.date_fetch,
            sourceOrder = chapter.source_order,
            lastModifiedAt = chapter.last_modified_at,
            version = chapter.version,
            isSyncing = chapter.is_syncing,
            memo = chapter.memo,
        )
    }

    @Suppress("UNUSED_PARAMETER")
    fun mapChapter(
        id: Long,
        entryId: Long,
        url: String,
        name: String,
        read: Boolean,
        bookmark: Boolean,
        chapterNumber: Double,
        scanlator: String?,
        dateUpload: Long,
        dateFetch: Long,
        sourceOrder: Long,
        lastModifiedAt: Long,
        version: Long,
        isSyncing: Boolean,
        memo: JsonObject,
    ): EntryChapter = EntryChapter(
        id = id,
        entryId = entryId,
        url = url,
        name = name,
        scanlator = scanlator,
        read = read,
        bookmark = bookmark,
        chapterNumber = chapterNumber,
        sourceOrder = sourceOrder,
        dateFetch = dateFetch,
        dateUpload = dateUpload,
        lastModifiedAt = lastModifiedAt,
        version = version,
        isSyncing = isSyncing,
        memo = memo,
    )
}
